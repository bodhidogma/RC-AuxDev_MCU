bool DevWS2812::Initialize(void) {
  Set_LED(0, 255, 0, 0);
  Set_LED(1, 0, 255, 0);
  Set_LED(2, 0, 0, 255);
  Set_LED(3, 46, 89, 128);
  Set_LED(4, 156, 233, 100);
  Set_LED(5, 102, 0, 235);
  Set_LED(6, 47, 38, 77);
  Set_LED(7, 255, 200, 0);

  datasentflag = 1;  // ready to send
  pat_step_ = 0;
  last_now_ms_ = 0;

  return true;
}

