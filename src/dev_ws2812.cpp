bool DevWS2812::Initialize(void) {
  SetLED(0, 255, 0, 0);
  SetLED(1, 0, 255, 0);
  SetLED(2, 0, 0, 255);
  SetLED(3, 46, 89, 128);
  SetLED(4, 156, 233, 100);
  SetLED(5, 102, 0, 235);
  SetLED(6, 47, 38, 77);
  SetLED(7, 255, 200, 0);

  dma_busy_ = 0;
  pat_step_ = 0;
  last_now_ms_ = 0;

  return true;
}

